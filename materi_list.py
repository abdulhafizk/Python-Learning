## --- List ---

# Kumpulan data numbers

data_angka = [1, 2, 3]
print(data_angka)

# Kumpulan data string
data_string = ["tear, husda", "aku"]
print(data_string)

# Kumpulan data boolean
data_boolean =[True, False, True, True]
print(data_boolean)

# Kumpulan data campuran
data_campuran = [1, "bala-bala", 2, "cireng", "tear", False, "husda", True ]
print(data_campuran)

# Cara Alternatif membuat list
data_range =  range(0,10,3) # range(start, stop, step)
print(data_range)
data_list = list(data_range)
print(data_list)

#membuat list dengan for loop, list chomperesion
list_pake_for = [i for i in range (0,10)]
print(list_pake_for)